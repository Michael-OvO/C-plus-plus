#include <iostream>
using namespace std;
// if statement
int main()
{

    // Relational Operators
    // True test
    if (3 < 6)
    {
        cout << "test is correct" << endl;
    }
    // false test
    if (3 > 6)
    {
        // the condition is false, and will not be printed
        cout << "test is false" << endl;
    }
    // quality operators
    if (3 == 3)
    {
        cout << "quality operator test!" << endl;
    }
    if (6 != 3)
    {
        cout << "6!=3!" << endl;
    }
}